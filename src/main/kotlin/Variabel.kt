fun main(args: Array<String>) {
    // Contoh variabel dinamis (tipe datanya ditentukan otomatis oleh Kotlin)
    var dynamciVar = 2
    println("Contoh dynamic variabel = " + dynamciVar)

    // Contoh variabel statis (tipe data ditentukan secara eksplisit: Char)
    var staticVar: Char = 'A'
    println("Contoh static variabel = " + staticVar)

}
