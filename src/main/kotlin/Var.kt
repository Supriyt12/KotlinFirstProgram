fun main(args: Array<String>) {
    // Deklarasi variabel dengan tipe Int tanpa langsung memberi nilai
    var varVariabel: Int

    // Memberikan nilai pertama (assigning first value)
    varVariabel = 56
    println("Pemberian nilai pertama pada varVariabel = " + varVariabel)

    // Memberikan nilai kedua (assigning second value), nilai lama ditimpa
    varVariabel = 78
    println("Pemberian nilai kedua pada varVariabel = " + varVariabel)
}
