fun main(args: Array<String>) {
    // Deklarasi variabel bertipe Int dengan nilai 5
    var intLiteral = 5

    // Deklarasi variabel bertipe Double dengan nilai 0.02
    var doubleLiteral = .02

    // Deklarasi variabel bertipe String dengan nilai "Hello"
    var stringLiteral = "Hello"

    // Deklarasi variabel bertipe Char dengan nilai '1'
    var charLiteral = '1'

    // Deklarasi variabel bertipe Boolean dengan nilai true
    var boolLiteral = true

    // Menampilkan nilai dari variabel intLiteral ke console
    println("intLiteral = " + intLiteral)

    // Menampilkan nilai dari variabel doubleLiteral ke console
    println("doubleLiteral = " + doubleLiteral)

    // Menampilkan nilai dari variabel stringLiteral ke console
    println("stringLiteral = " + stringLiteral)

    // Menampilkan nilai dari variabel charLiteral ke console
    println("charLiteral = " + charLiteral)

    // Menampilkan nilai dari variabel boolLiteral ke console
    println("boolLiteral = " + boolLiteral)
}
